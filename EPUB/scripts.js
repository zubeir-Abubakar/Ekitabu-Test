
function myFunction(){
	var s = document.getElementById('question1');
	var question1 = s.options[s.selectedIndex].value;

	if (question1 == 1) {
		document.getElementById("quiz").innerHTML="0/1";
	}
	else if (question1 == 2) {
			document.getElementById("quiz1").innerHTML="0/1";
	}
	else if (question1 == 3) {
		 document.getElementById("quiz1").innerHTML="1/1";
	}
}

